<h4><i class="glyphicon glyphicon-bookmark"></i> Incluindo anotações em uma aula</h4>
<br>
<p>No tópico anterior <mark>Gerenciar diários e disciplinas</mark> você irá ao painel da disciplina, 
  nele você poderá criar uma aula. Ao criar uma aula você terá acesso à opção de adicionar clicando em 
  <mark>Anotações de aula</mark>.</p>
<p>Você também poderá incluir uma anotação de aula ao acessar a aula que já foi criada clicando em seu título.</p>

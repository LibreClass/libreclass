<h4><i class="glyphicon glyphicon-bookmark"></i> Lançando presenças e faltas</h4>
<br>
<p>No tópico anterior <mark>Gerenciar diários e disciplinas</mark> você irá ao painel da disciplina, 
nele você poderá criar avaliação em <mark>Nova avaliação</mark>. Ao criar uma nova avaliação você será 
direcionado para uma tela onde aparecerá a lista de alunos cursando a disciplina e uma tabela para incluir 
a nota de cada aluno.</p>
<p>Para incluir a nota de cada aluno você pode digitar a nota e apertar <mark>ENTER</mark>, automaticamente 
você será direcinado para o campo de inserção de nota do próximo aluno.</p>

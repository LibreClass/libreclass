<p>Para vincular um professor a uma disciplina é necessário cadastrá-lo anteriormente</p> 
<p>Para vincular um professor a uma disciplina:</p>
<ol>
  <li>Clique em <strong>Minhas Turmas</strong></li>
  <li>Selecione uma turma</li>
  <li>Localize a disciplina para a qual deseja vincular o professor</li>
  <li>Nesta disciplina: clique em <i class="fa fa-link"/><strong>Vincular Professor</strong></li>
  <li>Preencha as informações necessárias</li>
  <li>Clique em <strong>Vincular</strong></li>
</ol>

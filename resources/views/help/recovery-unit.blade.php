<p>Uma recuperação é uma avaliação realizada por todos os alunos que não atingiram a média mínima do curso
para passar na unidade. O LibreClass calcula automaticamente a média dos alunos 
e gera a recuperação apenas com os alunos que devem realizá-la.</p>

<ol>
  <li>Clique em <strong>Meus Diários</strong></li>
  <li>Acesse o diário.</li>
  <li>Escolha uma unidade</li>
  <li>Para gerar a recuperação da unidade clique no botão <span class="btn btn-primary">Recuperação da Unidade</span></li>
    <img class="img-thumbnail center-block" alt="cursos" src="images/help/recovery/img-1.png">
  <br>
  <li>Informe os dados da sua avaliação e clique em <span class="btn btn-primary"><i class="fa fa-save"></i> Salvar</span></li>
    <img class="img-thumbnail center-block" alt="cursos" src="images/help/recovery/img-2.png">
  <br>
  <li>A avaliação de recuperação ficará disponível no painel do diário, no topo da coluna de avaliações.</li>
    <img class="img-thumbnail center-block" alt="cursos" src="images/help/recovery/img-3.png">  
  <br>
  <li></li>
</ol>

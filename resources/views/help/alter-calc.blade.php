<p>O cálculo de nota é definido para cada unidade. Existem 3 tipos de cálculos no Libreclass: Soma, Média Aritmética e Média Ponderada.</p>

<ol>
  <li>Clique em <strong>Meus Diários</strong></li>
  <li>Escolha o diário e selecione uma unidade.</li>
  <li>No painel da unidade, defina o tipo de cálculo.</li>

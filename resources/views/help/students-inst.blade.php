<p>É necessário que haja pelo menos um curso cadastrado para adicionar um aluno</p>
<p>Para adicionar um aluno:</p>
<ol>
  <li>Clique em <strong>Meus Alunos</strong></li>
  <li>Clique em <strong>+ Novo Aluno</strong></li>
  <li>Preencha os dados necessários</li>
  <li>Clique em <strong>Confirmar</strong></li>
</ol>

<h4><i class="glyphicon glyphicon-bookmark"></i> Criando a recuperação de uma unidade</h4>
<br>
<p>Uma recuperação é uma avaliação realizada por todos os alunos que não atingiram a média mínima 
especificada para passar na disciplina ofertada. O LibreClass calcula automaticamente a média dos alunos 
e gera a recuperação apenas com os alunos que devem realizá-la.</p>
<br>
<img class="img-thumbnail center-block" alt="cursos" src="images/help/recovery/img-1.png">
<br>
<p>Ao acessar uma disciplina ofertada você deve clicar em <mark>Recuperação da Unidade</mark>, você será direcionado 
para uma tela em que poderá escolher a data da avaliação, o instrumento utilizado para realizar a avaliação e 
adicinar comentários, se necessário.</p>
<br>
<img class="img-thumbnail center-block" alt="cursos" src="images/help/recovery/img-2.png">
<br>
<p>Ao criar uma recuperação a mesma estará disponível no painel da disciplina para que possa ser acessada e as notas 
das avaliações de recuperação adicionadas assim como se adiciona qualquer outra nota de avaliação no LibreClass.</p>

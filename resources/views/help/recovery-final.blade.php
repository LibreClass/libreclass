<p>Uma recuperação é uma avaliação realizada por todos os alunos que não atingiram a média mínima do curso
para passar na disciplina. O LibreClass calcula automaticamente a média dos alunos 
e gera a recuperação apenas com os alunos que devem realizá-la.</p>

<ol>
  <li>Clique em <strong>Meus Diários</strong></li>
  <li>Escolha o diário e clique em <span class="btn btn-default">Prova Final</span></li>
  <li>Informe os dados da avaliação e as notas do estudantes.</li>
  <li>Para finalizar clique em <span class="btn btn-primary"><i class="fa fa-save"></i> Salvar</span></li>

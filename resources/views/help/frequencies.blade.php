<h4><i class="glyphicon glyphicon-bookmark"></i> Lançando presenças e faltas</h4>
<br>
<p>No tópico anterior <mark>Gerenciar diários e disciplinas</mark> você irá ao painel da disciplina, 
nele você poderá criar uma aula. Ao criar uma aula você terá acesso a todos os alunos que pertencem 
à disciplina, podendo alterar o status de frequência entre <mark>P</mark> ou <mark>F</mark> ao clicar 
no botão que indica o status da presença.</p>

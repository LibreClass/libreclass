<h4><i class="glyphicon glyphicon-bookmark"></i> Incluindo plano de aula</h4>
<br>
<p>No tópico anterior <mark>Gerenciar diários e disciplinas</mark> você irá ao painel da disciplina, 
  nele você poderá criar uma aula. Ao criar uma aula você terá acesso à opção <mark>Inserir planejamento</mark>.</p>
<p>Lá você poderá incluir o plano de aula em um formulário intuitivo e preparado de acordo com as normas essenciais 
para a confecção de um bom plano de aula.</p>

<p>Uma unidade é automaticamente criada quando uma nova disciplina é registrada. Porém, mais unidades podem ser adicionadas</p> 
<p>Para criar uma unidade:</p>
<ol>
  <li>Clique em <strong>Minhas Turmas</strong></li>
  <li>Selecione uma turma</li>
  <li>Localize a disciplina para a qual deseja criar unidades</li>
  <li>Nesta disciplina: clique em <strong>+ Nova Unidade</strong></li>
  <li>Aguarde a mensagem de confirmação</li>
</ol>

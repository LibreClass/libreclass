<p>Um período (ano) só pode ser criado se houver pelo menos um curso cadastrado</p> 
<p>Para criar uma ano:</p>
<ol>
  <li>Clique em <strong>Meus Cursos</strong></li>
  <li>Clique em <strong>+ Novo Ano</strong></li>
  <li>Preencha os dados necessários</li>
  <li>Clique em <strong>Salvar</strong></li>
</ol>

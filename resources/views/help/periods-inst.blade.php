<p>Uma série só pode ser criada se haver pelo menos um curso cadastrado</p> 
<p>Para criar uma série:</p>
<ol>
  <li>Clique em <strong>Meus Cursos</strong></li>
  <li>Clique em <strong>+ Nova Série</strong></li>
  <li>Preencha os dados necessários</li>
  <li>Clique em <strong>Salvar</strong></li>
</ol>

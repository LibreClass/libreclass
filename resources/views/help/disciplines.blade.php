<h4><i class="glyphicon glyphicon-bookmark"></i> Acessando diários e disciplinas</h4>
<br>
<p>Para acessar os diários e disciplinas é necesário que você clique no menu no lado esquerdo.</p>
<br>
<img class="img-thumbnail center-block img-responsive" alt="cursos" src="images/help/disciplines/img-1.png">
<br>

<h4><i class="glyphicon glyphicon-bookmark"></i> Diários</h4>
<br>
<p>Ao clicar no menu <mark>Meus Diários</mark> você será movido para a página que lista todos os
seus diários. Um diário corresponde a uma área em que você poderá acessar a ementa de disciplina, 
a frequência dos alunos, unidades, resultados de prova final e resultados gerais da disciplina.</p>
<br>
<img class="img-thumbnail center-block" alt="cursos" src="images/help/disciplines/img-2.png">
<br>

<h4><i class="glyphicon glyphicon-bookmark"></i> Acesso a disciplinas ofertadas (ofertas)</h4>
<br>
<p>Ao clicar no <mark>nome da disciplina</mark> você será movido para o painel de controle
da disciplina, nele você poderá criar aulas, avaliações, gerar a recuperação da unidade ativa.</p>
<br>
